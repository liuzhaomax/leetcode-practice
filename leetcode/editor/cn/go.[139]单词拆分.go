func wordBreak(s string, wordDict []string) bool {
	if s == "" {
		return false
	}
	var jump int
	dp := make([]bool, len(wordDict)+1)
	dp[0] = true
	for i := 0; i < len(s); i++ {
		if i < jump {
			continue
		}
		for j := 0; j < len(wordDict); j++ {
			if i+len(wordDict[j]) <= len(s) && s[i:i+len(wordDict[j])] == wordDict[j] {
				dp[j+1] = dp[j]
				jump = i + len(wordDict[j])
				break
			}
		}
	}
	return dp[len(wordDict)]
}

